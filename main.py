def create_multirow_keyboard(items, items_per_row=2, callback_prefix='', back_button=None):
    markup = types.InlineKeyboardMarkup(row_width=items_per_row)

    # –†–∞–∑–±–∏–≤–∞–µ–º —ç–ª–µ–º–µ–Ω—Ç—ã –Ω–∞ —Å—Ç—Ä–æ–∫–∏
    for i in range(0, len(items), items_per_row):
        row_items = items[i:i + items_per_row]
        buttons = [types.InlineKeyboardButton(text, callback_data=f"{callback_prefix}{key}")
                   for key, text in row_items]
        markup.add(*buttons)

    if back_button:
        markup.add(types.InlineKeyboardButton(back_button['text'], callback_data=back_button['callback']))

    return markup


